package inheritance.lesson1;

public class Animal {
	
	private int age;
	private int size;
	private String food;
	
	public int getAge() {
		return age;
	}
	
	public int getSize() {
		return size;
	}
	
	public String getFood() {
		return food;
	}
	
	public void setAge(int newAge) {
		age = newAge;
	}
	
	public void setSize(int newSize) {
		size = newSize;
	}
	
	public void setFood(String newFood) {
		food = newFood;
	}

}
